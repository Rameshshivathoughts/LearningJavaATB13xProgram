package ex_14_For_Loop;

public class Lab110_For_Loop {
    public static void main(String[] args) {

        // Long

        for(long l=0l; l<10; l++){
            System.out.println(l);
        }


        //float

        for(float f=0.0f; f<10.67; f++){
            System.out.println(f);
        }
    }
}
