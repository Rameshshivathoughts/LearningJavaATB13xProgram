package ex_14_For_Loop;

public class Lab113_For_Loop_Confussion {
    public static void main(String[] args) {

        // Confusion

        for(int i = 0; i < 10; i++){            // 0 to 9  -- 10 times
            System.out.println(i);
        }


        for(int i = 1; i <= 10; i++){            // 1 to 10  -- 10 times
            System.out.println(i);
        }
    }
}
