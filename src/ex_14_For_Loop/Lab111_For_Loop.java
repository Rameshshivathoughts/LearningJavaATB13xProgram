package ex_14_For_Loop;

public class Lab111_For_Loop {
    public static void main(String[] args) {

        for (int i = -1; i > -10; i--){
            System.out.println(i);
        }



        for (int i = -1; i < -10; i--){
            System.out.println(i);
        }
        System.out.println("It will not go inside the for 2nd loop");
    }
}
