package ex_13_Switch;

import java.util.Scanner;

public class Lab100_Interview {
    public static void main(String[] args) {
        char code = 'A';
        switch (code){
            default -> System.out.println("heloooo");
            case 'B' -> System.out.println("65");
            case 'C' -> System.out.println("66");
        }
    }
}
