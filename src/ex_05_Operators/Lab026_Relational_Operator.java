package ex_05_Operators;

public class Lab026_Relational_Operator {
    public static void main(String[] args) {
        // <     less than
        // <=    less than or equal to
        // >     greater than
        // >=    greater than or equal to
        // ==    Equal to
        // !=    Not Equal to

        int a = 20;
        int b = 10;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
    }
}
