package ex_05_Operators;

public class Lab040_Logical_operator {
    public static void main(String[] args) {
        //logical operator
        int a =10;
        boolean b = true;
        System.out.println(b);
        System.out.println(!b);
        System.out.println(!!b);

        boolean c = true || false;
        System.out.println(c);

        boolean d = false && true;
        System.out.println(d);
    }
}
