package ex_05_Operators;

public class Lab038_All_Operator_4 {
    public static void main(String[] args) {
        //compound Operator

        int a = 10;
        a /= 5;

        System.out.println(a);

        int b = 20;
        b -= a;
        System.out.println(b);

        int c = 5;
        c *= 5+a;
        System.out.println(c);


    }
}
